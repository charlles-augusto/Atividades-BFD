# Script: listas_simples.py
# Descrição: Demonstração básica de criação e exibição de listas em Python
# Objetivo: Introduzir o conceito de listas e sua sintaxe básica

# Criação de uma lista simples com números inteiros
# Listas em Python são coleções ordenadas de elementos
# Podem conter elementos de diferentes tipos (int, str, float, etc.)
lista = [1, 2, 3, 4, 5]

# Exibição da lista completa
# print() mostra toda a lista em formato de lista Python
print("Lista completa:", lista)

# Informações adicionais sobre a lista criada:
print(f"Tipo da variável: {type(lista)}")
print(f"Quantidade de elementos: {len(lista)}")
print(f"Elementos: {lista}")

