#  Crie uma função chamada saudacao que receba o nome de uma pessoa como parâmetro e exiba a mensagem: "Olá, [nome]! Seja bem-vindo(a)!"
def saudacao(nome):
  """
  Esta função recebe o nome de uma pessoa e exibe uma mensagem de boas-vindas.
  """
  print(f"Olá, {nome}! Seja bem-vindo(a)!")

# Exemplo de uso:
saudacao("Ana")  # Saída: Olá, Ana! Seja bem-vindo(a)!
saudacao("Carlos") # Saída: Olá, Carlos! Seja bem-vindo(a)!